// (Model Context Protocol - Tool Definition)
// ============================================================
// KAGGLE REQUIREMENT: MCP Server / Tool
//
// PURPOSE: This is the "MCP Tool" that Agent 3 can call autonomously.
// In the Model Context Protocol paradigm, tools are well-defined
// functions with schemas that LLM agents can invoke to take
// real-world actions.
//
// This tool uses:
//   - PRIMARY:  Resend API (https://resend.com) — modern email API
//   - FALLBACK: Nodemailer SMTP — universal email fallback
//
// TOOL CONTRACT (Schema that Agent 3 understands):
//   Tool Name: send_email_alert
//   Parameters:
//     - patientEmail: string  (destination email)
//     - subject: string       (email subject line)
//     - message: string       (email body, may be HTML)
//     - alertType: string     ('CRITICAL' | 'WARNING' | 'REMINDER')
//   Returns:
//     - { success: boolean, messageId: string, provider: string }
//
// WHY THIS IS "MCP":
//   Agent 3 receives this tool's schema and decides WHEN to call it.
//   The agent doesn't blindly send emails — it reasons about whether
//   the pharmacist's findings justify an alert, then invokes this tool.
// ============================================================

import { Resend } from "resend";
import nodemailer from "nodemailer";

// Initialize Resend client
const resend = new Resend(process.env.RESEND_API_KEY);

// Initialize Nodemailer SMTP transport (fallback)
const smtpTransport = nodemailer.createTransport({
  host: process.env.SMTP_HOST || "smtp.gmail.com",
  port: parseInt(process.env.SMTP_PORT) || 587,
  secure: false,
  auth: {
    user: process.env.SMTP_USER,
    pass: process.env.SMTP_PASS,
  },
});

