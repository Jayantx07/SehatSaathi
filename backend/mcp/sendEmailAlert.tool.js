/**
 * MCP TOOL: send_email_alert
 * ============================================================
 * THE CORE MCP TOOL — Agent 3 calls this autonomously
 *
 * This function represents a "Model Context Protocol Tool" —
 * a well-defined, schema-validated action that an LLM agent
 * can invoke to cause a real-world side effect (sending an email).
 *
 * Agent 3 decides WHETHER to call this tool based on its reasoning.
 * If Agent 2 reports a CRITICAL drug interaction, Agent 3 will
 * autonomously invoke this tool without human intervention.
 *
 * @param {string} patientEmail - Recipient email address
 * @param {string} subject      - Email subject
 * @param {string} message      - Email body (plain text or HTML)
 * @param {string} alertType    - 'CRITICAL' | 'WARNING' | 'REMINDER'
 * @returns {Promise<Object>}   - Send result with provider info
 */
export async function sendEmailAlert(
  patientEmail,
  subject,
  message,
  alertType = "WARNING"
) {
  console.log(
    `📧 [MCP Tool: send_email_alert] Initiating ${alertType} email to: ${patientEmail}`
  );

  const htmlContent = generateEmailHTML(alertType, message);

  // ============================================================
  // ATTEMPT 1: Resend API (Primary Provider)
  // Resend is a modern email API with excellent deliverability
  // and developer experience. Preferred for hackathon demos.
  // ============================================================
  if (process.env.RESEND_API_KEY && process.env.RESEND_API_KEY !== "your_resend_api_key_here") {
    try {
      console.log("📧 [MCP Tool] Trying Resend API...");
      console.log('Routing email to verified developer address due to Sandbox limits.');
      const actualToEmail = process.env.VERIFIED_EMAIL || patientEmail;

      const { data, error } = await resend.emails.send({
        from: process.env.RESEND_FROM_EMAIL || "alerts@sehatsaathi.com",
        to: [actualToEmail],
        subject: subject,
        html: htmlContent,
        text: message, // Fallback plain text
      });

      if (error) {
        throw new Error(`Resend error: ${error.message}`);
      }

      console.log(`✅ [MCP Tool] Email sent via Resend. ID: ${data.id}`);
      return {
        success: true,
        messageId: data.id,
        provider: "Resend",
        alertType,
        timestamp: new Date().toISOString(),
      };
    } catch (resendError) {
      console.warn(
        `⚠️ [MCP Tool] Resend failed: ${resendError.message}. Trying SMTP fallback...`
      );
    }
  }

  // ============================================================
  // ATTEMPT 2: Nodemailer SMTP (Fallback Provider)
  // Universal fallback using standard SMTP protocol.
  // Works with Gmail, SendGrid, Mailgun, and any SMTP server.
  // ============================================================
  if (process.env.SMTP_USER && process.env.SMTP_USER !== "your_email@gmail.com") {
    try {
      console.log("📧 [MCP Tool] Trying Nodemailer SMTP fallback...");

      const mailOptions = {
        from: `"Sehat Saathi" <${process.env.SMTP_USER}>`,
        to: patientEmail,
        subject: subject,
        text: message,
        html: htmlContent,
      };

      const info = await smtpTransport.sendMail(mailOptions);

      console.log(`✅ [MCP Tool] Email sent via SMTP. ID: ${info.messageId}`);
      return {
        success: true,
        messageId: info.messageId,
        provider: "Nodemailer-SMTP",
        alertType,
        timestamp: new Date().toISOString(),
      };
    } catch (smtpError) {
      console.error(`❌ [MCP Tool] SMTP fallback also failed: ${smtpError.message}`);
      throw new Error(`All email providers failed. Last error: ${smtpError.message}`);
    }
  }

  // ============================================================
  // DEMO MODE: Simulate email sending when no keys are configured
  // ============================================================
  console.log(
    "📧 [MCP Tool: DEMO MODE] Simulating email send (no real keys configured)"
  );
  return {
    success: true,
    messageId: `demo-${Date.now()}`,
    provider: "Demo-Simulation",
    alertType,
    timestamp: new Date().toISOString(),
    note: "Email simulated — configure RESEND_API_KEY or SMTP credentials to send real emails",
  };
}

